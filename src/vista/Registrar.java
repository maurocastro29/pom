    private void btn_RegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RegistrarseActionPerformed
        EstudianteController ec = new EstudianteController();
        String nombre = jTF_Nombre.getText();
        String usuario = jTF_Usuario.getText();
        String codigo = jTF_Codigo.getText();
        String password = new String(jPassword.getPassword());
        String confirmar = new String(jConfirmar.getPassword());
        if(nombre.equals("")||usuario.equals("")||codigo.equals("")||password.equals("")||confirmar.equals("")){
            JOptionPane.showMessageDialog(null, "Alguno de los campos estan vacios, Rellenelos para continuar");
        }else{
            if(ec.existeCodigo(codigo)){
                    JOptionPane.showMessageDialog(null, "Este codigo ya esta en uso, por favor ingrese uno diferente");
            }else{
                if(ec.existeUsuario(usuario)){
                    JOptionPane.showMessageDialog(null, "Este usuario ya esta existe, por favor ingrese uno diferente");
                }else{
                    if(!password.equals(confirmar)){
                        JOptionPane.showMessageDialog(null, "Las contrase単as no coinciden, vuelvalo a intentar.");
                    }else{
                        if(password.length()>20){
                            JOptionPane.showMessageDialog(null, "La contrase単a no debe superar los 20 caracteres");
                        }else{
                            ec.insertar(nombre, usuario, password, codigo);
                            jTF_Nombre.setText("");
                            jTF_Codigo.setText("");
                            jTF_Usuario.setText("");
                            jPassword.setText("");
                            jConfirmar.setText("");
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btn_RegistrarseActionPerformed

